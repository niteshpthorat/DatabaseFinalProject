package pojo;

public class InstrumentPlayer {
    private int id;
    private int idInstrument;  
    private String musSSN;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the idInstrument
	 */
	public int getIdInstrument() {
		return idInstrument;
	}
	/**
	 * @param idInstrument the idInstrument to set
	 */
	public void setIdInstrument(int idInstrument) {
		this.idInstrument = idInstrument;
	}
	/**
	 * @return the musSSN
	 */
	public String getMusSSN() {
		return musSSN;
	}
	/**
	 * @param musSSN the musSSN to set
	 */
	public void setMusSSN(String musSSN) {
		this.musSSN = musSSN;
	}
	public InstrumentPlayer() {
		super();
	}  


}
